package org.perscholas;

public class EscapeSequenceExample {
    public static void main(String[] args) {
        System.out.print("This is line 1\n");
        System.out.print("This is line 2");
        System.out.println("This is line 3");

        System.out.println("This is a tab\t");

        System.out.println("This is an example for double quotes\"");
        System.out.println("This is an example for including \\ backslash");

        String a ="abc";

        System.out.println(a.toUpperCase());
        System.out.println(a.isEmpty());

    }


}
